def get_all_vn_stocks() -> list[dict]:
    """Get all VN stocks with standardized format."""
    return [
        {
            "symbol": s["symbol"],
            "name": s["name"],
            "market": "vn",
            "exchange": s.get("exchange", "HOSE"),
            "currency": "VND",
            "is_default": s["symbol"] in ["FPT", "VNM", "VIC", "HPG", "MBB", "TCB", "VHM", "MSN", "VCB", "SSI", "VND", "GAS", "PLX", "STB", "ACB", "BID", "CTG", "VRE", "MWG", "PNJ", "REE"],
        }
        for s in VN_STOCKS
    ]


def get_default_vn_stocks() -> list[dict]:
    """Get default VN stocks for quick picks."""
    defaults = [
        "FPT", "VNM", "VIC", "HPG", "MBB", "TCB", "VHM", "MSN", "VCB", "SSI",
        "VND", "GAS", "PLX", "STB", "ACB", "BID", "CTG", "VRE", "MWG", "PNJ",
        "REE", "PVS", "PVD", "VNM", "VRE", "HPG", "FPT", "TCB", "VPB", "CTG"
    ]
    seen = set()
    result = []
    for sym in defaults:
        if sym not in seen:
            for s in VN_STOCKS:
                if s["symbol"] == sym:
                    result.append({
                        "symbol": sym,
                        "name": s["name"],
                        "market": "vn",
                        "exchange": s.get("exchange", "HOSE"),
                        "currency": "VND",
                        "is_default": True,
                    })
                    seen.add(sym)
                    break
    return result
